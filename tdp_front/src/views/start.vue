<h2>交通流数据处理工具箱</h2>
<p class="text">交通流数据处理工具箱主要为三维数字孪生场景中交通流效果提供数据预处理能力，保障三维数字孪生场景中的交通流运行平滑和稳定，该工具箱可将动静态的交通流数据进行数据整合和传输频率规整、跨传感器轨迹整合、数据落盘存储、轨迹纠偏以及根据场景镜头位置进行轨迹调度。</p>
