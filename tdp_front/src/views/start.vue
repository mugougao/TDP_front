<h3>跨传感器轨迹融合</h3>
<p class="text">在多路雷视融合感知设备的道路场景中，常常出现感知设备的感知区域重叠的情况，这会导致同一个目标会以不同的id出现两次，此功能开启后，我们会通过高性能的实时位置计算，将相对距离小于一定范围的目标识别为相同目标，将冗余数据删除。</p>
<h3>轨迹数据落盘存储</h3>
<p class="text">支持将轨迹数据实时存入Mysql数据库，可对存储时间进行管理，超出存储时间后，自动对数据进行删除。数据库需要用户自行配置</p>
<h3>轨迹纠偏</h3>
<p class="text">支持依据高精路网或车道中心线矢量数据（可由公路桩号坐标计算工具输出）对车辆轨迹进行纠偏，保证车辆始终运行在车道中心线上，不会出现由于数据质量造成的左右闪动，同时会保证车辆变道的平滑效果</p>
<h3>轨迹调度</h3>
<p class="text">当场景中的车辆超过2000辆时，由于硬件设备性能瓶颈限制，三维数字孪生场景可能出现卡顿，此功能开启后，会根据镜头位置对镜头外的数据进行动态剔除，减轻硬件压力，提高场景流畅性</p>
